void List_Move(int8 New_Data)
// 对数组进行移位
{
  static int32 Temp_i;
  int32 Temp_j;
  if(Temp_i<120)
    Temp_List[Temp_i]=New_Data;
  else
  {
    for(Temp_j=0;Temp_j<119;Temp_j++)
      Temp_List[Temp_j]=Temp_List[Temp_j+1];  
    Temp_List[119]=New_Data;  
  }

  if(Temp_i>=120)
    Temp_i=120;
  else
    Temp_i++; 
}
