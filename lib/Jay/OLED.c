/************将浮点型数据转换为字符串************/
//fNum待变换浮点数
//str[]存储转换后数组
//dotsize 保留几位小数
int Float_To_String(float fNum,uint8 str[],int dotsize)
{
	// 定义变量
	int iSize = 0;//记录字符串长度的数
	int n = 0;//用作循环的临时变量
	uint8 *p = str;//做换向时用的指针
	uint8 *s = str;//做换向时用的指针
	int8 isnegative = 0;//负数标志
	uint32 i_predot;//小数点前的数
	uint32 i_afterdot;//小数点后的数
	float f_afterdot;//实数型的小数部分

	//判断是否为负数     
	if(fNum < 0)
	{
		isnegative = 1;//设置负数标志
		fNum = 0 - fNum;//将负数变为正数
	}

	i_predot = (uint32)fNum;//将小数点之前的数变为整数
	f_afterdot = fNum - i_predot;//单独取出小数点之后的数
	//根据设定的要保存的小数点后的位数，将小数点后相应的位数变到小数点之前
	for(n = dotsize;n > 0;n--)
	{
	        f_afterdot = f_afterdot*10;
	}
	i_afterdot = (uint32)f_afterdot;//将小数点后相应位数的数字变为整数

	//先将小数点后的数转换为字符串
	n=dotsize;
	while(i_afterdot > 0 |n > 0)
	{
		n--;
	 	str[iSize++] = i_afterdot % 10 + '0';//对10取余并变为ASCII码
	 	i_afterdot = i_afterdot / 10;//对10取商
	}
        if(dotsize==0)
        {
          str[iSize++] = ' ';//小数点是0的时候，用空格替代
        }
        else
        {
	str[iSize++] = '.';//加上小数点
        }
    //处理小数点前为0的情况。
	if(i_predot == 0)
		str[iSize++] = '0';
	//再将小数点前的数转换为字符串
	while(i_predot > 0)
	{
	 	str[iSize++] = i_predot%10 + '0';//对10取余并变为ASCII码
	 	i_predot = i_predot / 10;//对10取商
	}

	if(isnegative == 1)
	str[iSize++] = '-';//如果是负数，则在最后加上负号
	str[iSize] = '\0';//加上字符串结束标志
	
	p = str + iSize - 1;//将P指针指向字符串结束标志之前

	for( ;p - s > 0;p--,s++)//将字符串中存储的数调头   
	{   
		*s^=*p;   
		*p^=*s;   
		*s^=*p;   
	}   
	// 返回指针字符串大小 
	return iSize;
}

